import mysql from 'serverless-mysql';
import pg from 'pg';

const { Pool } = pg;

function inferDbProvider() {
  const explicitProvider = process.env.DB_PROVIDER || process.env.DATABASE_PROVIDER;
  if (explicitProvider) return explicitProvider.toLowerCase();
  if (
    process.env.SUPABASE_DB_URL ||
    process.env.SUPABASE_DATABASE_URL ||
    process.env.POSTGRES_URL ||
    process.env.DATABASE_URL
  ) {
    return 'postgres';
  }
  return 'mysql';
}

export const dbProvider = inferDbProvider();
export const isPostgresMode = dbProvider === 'supabase' || dbProvider === 'postgres' || dbProvider === 'postgresql';
export const dbModeLabel = isPostgresMode ? 'postgres' : 'mysql';

function getPostgresConnectionString() {
  return (
    process.env.SUPABASE_DB_URL ||
    process.env.SUPABASE_DATABASE_URL ||
    process.env.POSTGRES_URL ||
    process.env.DATABASE_URL ||
    ''
  );
}

const dbConfig = {
  host: process.env.MYSQL_HOST,
  port: process.env.MYSQL_PORT,
  database: process.env.MYSQL_DATABASE,
  user: process.env.MYSQL_USER,
  password: process.env.MYSQL_PASSWORD,
  connectTimeout: parseInt(process.env.MYSQL_CONNECT_TIMEOUT || '6000', 10)
};

export function getDatabaseConfigStatus() {
  if (isPostgresMode) {
    return {
      provider: dbModeLabel,
      configured: Boolean(getPostgresConnectionString()),
      missing: getPostgresConnectionString() ? [] : ['SUPABASE_DB_URL or POSTGRES_URL or DATABASE_URL']
    };
  }

  const missing = [];
  if (!process.env.MYSQL_HOST) missing.push('MYSQL_HOST');
  if (!process.env.MYSQL_DATABASE) missing.push('MYSQL_DATABASE');
  if (!process.env.MYSQL_USER) missing.push('MYSQL_USER');
  if (!process.env.MYSQL_PASSWORD) missing.push('MYSQL_PASSWORD');
  return {
    provider: dbModeLabel,
    configured: missing.length === 0,
    missing
  };
}

// 保持原有导出的兼容性，同时提供一个可以热重建的 activeDb
export const db = mysql({ config: dbConfig });
let activeDb = db;
let queryQueue = Promise.resolve();

let pgPool = null;

function getPgPool() {
  if (!pgPool) {
    const connectionString = getPostgresConnectionString();
    if (!connectionString) {
      throw new Error('Supabase/Postgres mode requires SUPABASE_DB_URL, SUPABASE_DATABASE_URL, POSTGRES_URL, or DATABASE_URL.');
    }

    pgPool = new Pool({
      connectionString,
      ssl: process.env.SUPABASE_DB_SSL === 'false' ? false : { rejectUnauthorized: false },
      max: parseInt(process.env.POSTGRES_POOL_MAX || '8', 10),
      idleTimeoutMillis: parseInt(process.env.POSTGRES_IDLE_TIMEOUT || '30000', 10),
      connectionTimeoutMillis: parseInt(process.env.POSTGRES_CONNECT_TIMEOUT || '8000', 10)
    });
  }
  return pgPool;
}

function replaceMysqlPlaceholders(sql) {
  let index = 0;
  let inSingle = false;
  let inDouble = false;
  let escaped = false;

  return sql.replace(/./g, (char) => {
    if (escaped) {
      escaped = false;
      return char;
    }
    if (char === '\\') {
      escaped = true;
      return char;
    }
    if (char === "'" && !inDouble) {
      inSingle = !inSingle;
      return char;
    }
    if (char === '"' && !inSingle) {
      inDouble = !inDouble;
      return char;
    }
    if (char === '?' && !inSingle && !inDouble) {
      index += 1;
      return `$${index}`;
    }
    return char;
  });
}

function normalizePostgresSql(sql) {
  let nextSql = sql.trim();

  nextSql = nextSql.replace(
    /ON\s+DUPLICATE\s+KEY\s+UPDATE\s+known_facts\s*=\s*VALUES\(known_facts\)\s*,\s*missing_fields\s*=\s*VALUES\(missing_fields\)/ig,
    'ON CONFLICT (session_id) DO UPDATE SET known_facts = EXCLUDED.known_facts, missing_fields = EXCLUDED.missing_fields, updated_at = CURRENT_TIMESTAMP'
  );

  nextSql = nextSql.replace(
    /ON\s+DUPLICATE\s+KEY\s+UPDATE\s+summary\s*=\s*VALUES\(summary\)\s*,\s*maturity_score\s*=\s*VALUES\(maturity_score\)\s*,\s*pain_points\s*=\s*VALUES\(pain_points\)\s*,\s*opportunity_map\s*=\s*VALUES\(opportunity_map\)\s*,\s*recommended_agents\s*=\s*VALUES\(recommended_agents\)\s*,\s*roadmap_30_60_90\s*=\s*VALUES\(roadmap_30_60_90\)\s*,\s*risks\s*=\s*VALUES\(risks\)\s*,\s*data_requirements\s*=\s*VALUES\(data_requirements\)\s*,\s*next_actions\s*=\s*VALUES\(next_actions\)/ig,
    'ON CONFLICT (session_id) DO UPDATE SET summary = EXCLUDED.summary, maturity_score = EXCLUDED.maturity_score, pain_points = EXCLUDED.pain_points, opportunity_map = EXCLUDED.opportunity_map, recommended_agents = EXCLUDED.recommended_agents, roadmap_30_60_90 = EXCLUDED.roadmap_30_60_90, risks = EXCLUDED.risks, data_requirements = EXCLUDED.data_requirements, next_actions = EXCLUDED.next_actions'
  );

  nextSql = nextSql.replace(/\bAUTO_INCREMENT\b/ig, 'GENERATED BY DEFAULT AS IDENTITY');
  nextSql = nextSql.replace(/\bDATETIME\b/ig, 'TIMESTAMPTZ');
  nextSql = nextSql.replace(/\bLONGTEXT\b/ig, 'TEXT');
  nextSql = nextSql.replace(/\bINT\b/ig, 'INTEGER');
  nextSql = nextSql.replace(/ENGINE\s*=\s*InnoDB\s*/ig, '');
  nextSql = nextSql.replace(/DEFAULT\s+CHARSET\s*=\s*utf8mb4/ig, '');
  nextSql = nextSql.replace(/AFTER\s+\w+/ig, '');
  nextSql = nextSql.replace(/ON\s+UPDATE\s+CURRENT_TIMESTAMP/ig, '');

  if (/^\s*INSERT\s+INTO\s+draw_images\b/i.test(nextSql) && !/\bRETURNING\b/i.test(nextSql)) {
    nextSql += ' RETURNING id';
  }

  return replaceMysqlPlaceholders(nextSql);
}

async function runPostgresQuery(q, values = []) {
  const sql = normalizePostgresSql(q);
  const result = await getPgPool().query(sql, values);
  const command = (result.command || '').toUpperCase();

  if (command === 'SELECT') {
    return result.rows;
  }

  if (command === 'INSERT' && result.rows?.[0]?.id) {
    return { affectedRows: result.rowCount, insertId: result.rows[0].id, rows: result.rows };
  }

  return { affectedRows: result.rowCount, rowCount: result.rowCount, rows: result.rows || [] };
}

function withTimeout(promise, ms, label) {
  let timer;
  const timeout = new Promise((_, reject) => {
    timer = setTimeout(() => {
      const error = new Error(`${label} timed out after ${ms}ms`);
      error.code = 'ETIMEDOUT';
      reject(error);
    }, ms);
  });
  return Promise.race([promise, timeout]).finally(() => clearTimeout(timer));
}

// 精准甄别哪些底层错误属于连接/网络抖动（需要重试），哪些属于语法/业务逻辑错误（无需重试）
function shouldRetry(error) {
  if (!error) return false;
  const code = error.code || '';
  const message = error.message || '';

  // 1. 明确的业务/语法级错误，严禁重试
  const nonRetryCodes = [
    'ER_DUP_FIELDNAME',      // Duplicate column name
    'ER_TABLE_EXISTS_ERROR', // Table already exists
    'ER_DUP_ENTRY',          // Duplicate entry (e.g. Unique key constraint)
    'ER_BAD_FIELD_ERROR',    // Unknown column
    'ER_NO_SUCH_TABLE',      // Table doesn't exist
    'ER_PARSE_ERROR',        // Syntax error
    'ER_DATA_TOO_LONG',      // Data too long
    'ER_NO_REFERENCED_ROW',  // Foreign key constraint failed
    'ER_NO_REFERENCED_ROW_2'
  ];
  if (nonRetryCodes.includes(code)) {
    return false;
  }

  // 2. 明确的连接与网络丢失类错误，应当重试
  const retryCodes = [
    'PROTOCOL_CONNECTION_LOST',
    'ECONNREFUSED',
    'ETIMEDOUT',
    'ENOTFOUND',
    'ECONNRESET',
    'EPIPE',
    'ER_CON_COUNT_ERROR'
  ];
  if (retryCodes.includes(code)) {
    return true;
  }

  // 3. 模糊匹配可能包含的网络/连接超时/状态关闭相关的信息
  const lowerMsg = message.toLowerCase();
  if (
    lowerMsg.includes('timeout') ||
    lowerMsg.includes('closed state') ||
    lowerMsg.includes('connection') ||
    lowerMsg.includes('econn') ||
    lowerMsg.includes('etimedout')
  ) {
    return true;
  }

  return false;
}

async function runQuery(q, values) {
  const maxRetries = 2;
  const queryTimeout = parseInt(process.env.MYSQL_QUERY_TIMEOUT || '8000', 10);
  let lastError;
  
  for (let attempt = 1; attempt <= maxRetries; attempt++) {
    try {
      const results = await withTimeout(activeDb.query(q, values), queryTimeout, 'Database query');
      return results;
    } catch (error) {
      lastError = error;
      
      // 如果属于业务或语法类报错，无需重试，立即向上抛出以提高响应效率
      if (!shouldRetry(error)) {
        throw error;
      }
      
      console.warn(`[Database query] Attempt ${attempt} failed (will retry): ${error.message || error}`);
      
      // 自愈重构：一旦检测到连接已关闭或超时断开，立刻抛弃坏池，重新配置健康的新 client 池！
      const errMsg = (error.message || '').toLowerCase();
      if (
        errMsg.includes('closed state') ||
        errMsg.includes('connection') ||
        errMsg.includes('timeout') ||
        error.code === 'PROTOCOL_CONNECTION_LOST' ||
        error.code === 'ETIMEDOUT'
      ) {
        console.warn(`[Database query] Detected dead pool state "${error.message}". Re-initializing active client pool via quit().`);
        try {
          activeDb.quit(); // 清空并重置底层私有 pool 变量为 null
        } catch (e) {}
        activeDb = mysql({ config: dbConfig });
      }
      
      if (attempt < maxRetries) {
        // 退避延迟：300ms
        const delay = attempt * 300;
        await new Promise(resolve => setTimeout(resolve, delay));
      }
    }
  }
  
  throw lastError;
}

export async function query(q, values = []) {
  if (isPostgresMode) {
    return runPostgresQuery(q, values);
  }

  const task = () => runQuery(q, values);
  const result = queryQueue.then(task, task);
  queryQueue = result.catch(() => {});
  return result;
}
